-- =====================================================
-- CREACIÓN DE TABLAS
-- =====================================================

-- TABLA: ESTADO
CREATE TABLE ESTADO (
    ESTADO_ID_PK INT PRIMARY KEY,
    DESCRIPCION VARCHAR(50) NOT NULL
);

-- TABLA: ROL (3 registros)
CREATE TABLE ROL (
    ROL_ID_PK INT PRIMARY KEY,
    DESCRIPCION VARCHAR(50) NOT NULL
);

-- TABLA: USUARIO
CREATE TABLE USUARIO (
    USUARIO_ID_PK INT PRIMARY KEY,
    ESTADO_ID_FK INT,
    ROL_FK INT,
    EMAIL VARCHAR(100) UNIQUE NOT NULL,
    CONTRASENA VARCHAR(255) NOT NULL,
    FOREIGN KEY (ESTADO_ID_FK) REFERENCES ESTADO(ESTADO_ID_PK),
    FOREIGN KEY (ROL_FK) REFERENCES ROL(ROL_ID_PK)
);

-- TABLA: EVALUACION (valoraciones de usuarios)
CREATE TABLE EVALUACION (
    EVALUACION_ID_PK INT AUTO_INCREMENT PRIMARY KEY,
    USUARIO_ID_FK INT NOT NULL,
    RATING INT NOT NULL,
    COMENTARIO TEXT NOT NULL,
    FECHA_CREADO DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (USUARIO_ID_FK) REFERENCES USUARIO(USUARIO_ID_PK)
);

-- TABLA: DIRECCION
CREATE TABLE DIRECCION (
    DIRECCION_ID_PK INT PRIMARY KEY,
    CANTON VARCHAR(100) NOT NULL,
    DISTRITO VARCHAR(100) NOT NULL,
    DIRECCION_EXACTA VARCHAR(255) NOT NULL
);

-- TABLA: CLIENTE
CREATE TABLE CLIENTE (
    CLIENTE_ID_PK INT PRIMARY KEY,
    USUARIO_ID_FK INT,
    DIRECCION_ID_FK INT,
    NOMBRE VARCHAR(50) NOT NULL,
    APELLIDO_1 VARCHAR(50) NOT NULL,
    APELLIDO_2 VARCHAR(50),
    TELEFONO VARCHAR(20),
    FOREIGN KEY (USUARIO_ID_FK) REFERENCES USUARIO(USUARIO_ID_PK),
    FOREIGN KEY (DIRECCION_ID_FK) REFERENCES DIRECCION(DIRECCION_ID_PK)
);

-- TABLA: ESPECIE
CREATE TABLE ESPECIE (
    ESPECIE_ID_PK INT PRIMARY KEY,
    ESPECIE VARCHAR(50) NOT NULL
);

-- TABLA: RAZA
CREATE TABLE RAZA (
    RAZA_ID_PK INT PRIMARY KEY,
    ESPECIE_ID_FK INT,
    NOMBRE VARCHAR(50) NOT NULL,
    FOREIGN KEY (ESPECIE_ID_FK) REFERENCES ESPECIE(ESPECIE_ID_PK)
);

-- TABLA: ANIMAL
CREATE TABLE ANIMAL (
    ANIMAL_ID_PK INT PRIMARY KEY,
    RAZATB_ID_FK INT,
    ESTADO_ID_FK INT,
    CLIENTE_ID_FK INT,
    NOMBRE VARCHAR(50) NOT NULL,
    PESO DECIMAL(5,2),
    EDAD INT,
    SEXO CHAR(1),
    OBSERVACIONES TEXT,
    FOREIGN KEY (RAZATB_ID_FK) REFERENCES RAZA(RAZA_ID_PK),
    FOREIGN KEY (ESTADO_ID_FK) REFERENCES ESTADO(ESTADO_ID_PK),
    FOREIGN KEY (CLIENTE_ID_FK) REFERENCES CLIENTE(CLIENTE_ID_PK)
);

-- TABLA: TIPO_DE_CITA
CREATE TABLE TIPO_DE_CITA (
    TIPO_DE_CITA_ID_PK INT PRIMARY KEY,
    DESCRIPCION VARCHAR(100) NOT NULL
);

-- TABLA: VETERINARIO (3 registros)
CREATE TABLE VETERINARIO (
    VETERINARIO_ID_PK INT PRIMARY KEY,
    USUARIO_ID_FK INT,
    DIRECCION_ID_FK INT,
    NOMBRE VARCHAR(50) NOT NULL,
    PRIMER_APELLIDO VARCHAR(50) NOT NULL,
    SEGUNDO_APELLIDO VARCHAR(50),
    TELEFONO VARCHAR(20),
    ESPECIALIDAD VARCHAR(100),
    FOREIGN KEY (USUARIO_ID_FK) REFERENCES USUARIO(USUARIO_ID_PK),
    FOREIGN KEY (DIRECCION_ID_FK) REFERENCES DIRECCION(DIRECCION_ID_PK)
);

-- TABLA: CITA
CREATE TABLE CITA (
    CITA_ID_PK INT PRIMARY KEY,
    ANIMAL_ID_FK INT,
    ESTADO_ID_FK INT,
    VETERINARIO_ID_FK INT,
    TIPO_DE_CITA_ID_FK INT,
    FECHA DATE NOT NULL,
    HORA_DE_INICIO TIME NOT NULL,
    HORA_DE_FINALIZACION TIME,
    NOTAS_VETERINARIO TEXT,
    FOREIGN KEY (ANIMAL_ID_FK) REFERENCES ANIMAL(ANIMAL_ID_PK),
    FOREIGN KEY (ESTADO_ID_FK) REFERENCES ESTADO(ESTADO_ID_PK),
    FOREIGN KEY (VETERINARIO_ID_FK) REFERENCES VETERINARIO(VETERINARIO_ID_PK),
    FOREIGN KEY (TIPO_DE_CITA_ID_FK) REFERENCES TIPO_DE_CITA(TIPO_DE_CITA_ID_PK)
);

-- TABLA: SERVICIO
CREATE TABLE SERVICIO (
    SERVICIO_ID_PK INT PRIMARY KEY,
    ESTADOTB_ID_FK INT,
    NOMBRE VARCHAR(100) NOT NULL,
    DESCRIPCION TEXT,
    PRECIO DECIMAL(10,2) NOT NULL,
    DURACION INT COMMENT 'Duración en minutos',
    ICONO VARCHAR(32) NULL,
    FOREIGN KEY (ESTADOTB_ID_FK) REFERENCES ESTADO(ESTADO_ID_PK)
);

-- TABLA INTERMEDIA: SERVICIOS_POR_CITA
CREATE TABLE SERVICIOS_POR_CITA (
    CITA_ID_FK INT,
    SERVICIO_ID_FK INT,
    PRIMARY KEY (CITA_ID_FK, SERVICIO_ID_FK),
    FOREIGN KEY (CITA_ID_FK) REFERENCES CITA(CITA_ID_PK),
    FOREIGN KEY (SERVICIO_ID_FK) REFERENCES SERVICIO(SERVICIO_ID_PK)
);

-- TABLA: FACTURA
CREATE TABLE FACTURA (
    FACTURA_ID_PK INT PRIMARY KEY,
    CITA_ID_FK INT,
    ESTADO_ID_FK INT,
    TOTAL_A_COBRAR DECIMAL(10,2) NOT NULL,
    FECHA_EMISION DATETIME NOT NULL,
    FOREIGN KEY (CITA_ID_FK) REFERENCES CITA(CITA_ID_PK),
    FOREIGN KEY (ESTADO_ID_FK) REFERENCES ESTADO(ESTADO_ID_PK)
);

-- TABLA: METODO_PAGO (5 registros)
CREATE TABLE METODO_PAGO (
    METODOPAGO_ID_PK INT PRIMARY KEY,
    METODO VARCHAR(50) NOT NULL
);

-- TABLA: PAGO
CREATE TABLE PAGO (
    PAGO_ID_PK INT PRIMARY KEY,
    ESTADO_ID_FK INT,
    FACTURA_ID_FK INT,
    METODOPAGO_ID_FK INT,
    CITA_ID_FK INT,
    MONTO_TOTAL DECIMAL(10,2) NOT NULL,
    FECHA_DE_PAGO DATETIME NOT NULL,
    FOREIGN KEY (ESTADO_ID_FK) REFERENCES ESTADO(ESTADO_ID_PK),
    FOREIGN KEY (FACTURA_ID_FK) REFERENCES FACTURA(FACTURA_ID_PK),
    FOREIGN KEY (METODOPAGO_ID_FK) REFERENCES METODO_PAGO(METODOPAGO_ID_PK),
    FOREIGN KEY (CITA_ID_FK) REFERENCES CITA(CITA_ID_PK)
);

-- TABLA: TIPO_DE_MEDICAMENTO
CREATE TABLE TIPO_DE_MEDICAMENTO (
    TIPO_DE_MEDICAMENTO_PK INT PRIMARY KEY,
    ESTADO_ID_FK INT,
    DESCRIPCION VARCHAR(100) NOT NULL,
    FOREIGN KEY (ESTADO_ID_FK) REFERENCES ESTADO(ESTADO_ID_PK)
);

-- TABLA: MEDICAMENTO
CREATE TABLE MEDICAMENTO (
    MEDICAMENTO_CODIGO_PK INT PRIMARY KEY,
    ESTADO_ID_FK INT,
    TIPO_DE_MEDICAMENTO_FK INT,
    NOMBRE VARCHAR(100) NOT NULL,
    CANTIDAD_DISPONIBLE INT NOT NULL,
    FOREIGN KEY (ESTADO_ID_FK) REFERENCES ESTADO(ESTADO_ID_PK),
    FOREIGN KEY (TIPO_DE_MEDICAMENTO_FK) REFERENCES TIPO_DE_MEDICAMENTO(TIPO_DE_MEDICAMENTO_PK)
);

